// NEDERLANDS → GAY-SLANG automatisch maken
const nlToGay = {};
for (const [gay, nl] of Object.entries(gayToNl)) {
  const lowerNl = nl.toLowerCase();
  // alleen als die NL-zin nog niet bestaat
  if (!nlToGay[lowerNl]) {
    nlToGay[lowerNl] = gay;
  }
}

// vertalen
function translate(text, direction) {
  let result = text;
  const dict = direction === "nl-to-gay" ? nlToGay : gayToNl;

  // langste sleutels eerst
  const entries = Object.entries(dict).sort((a, b) => b[0].length - a[0].length);

  for (const [from, to] of entries) {
    const needsLooseMatch =
      from.includes(" ") ||
      from.includes("’") ||
      from.includes("'") ||
      from.includes(".") ||
      from.includes("…");

    const pattern = escapeRegex(from);
    const regex = needsLooseMatch
      ? new RegExp(pattern, "gi")
      : new RegExp("\\b" + pattern + "\\b", "gi");

    result = result.replace(regex, (match) => {
      // simpele hoofdletter-check
      if (match[0] === match[0].toUpperCase()) {
        return to.charAt(0).toUpperCase() + to.slice(1);
      }
      return to;
    });
  }

  return result;
}

// helper
function escapeRegex(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

