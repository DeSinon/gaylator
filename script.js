function checkForJumpscare(text) {
  // allemaal lowercase
  const triggers = ["annesophie", "spook", "desinon"]; 

  const lower = text.toLowerCase();

  // check of één van de woorden erin zit
  const shouldScare = triggers.some(word => lower.includes(word));
  if (!shouldScare) return;

  const layer = document.getElementById("jumpscare");
  const sound = document.getElementById("jumpscare-sound");

  if (layer) {
    layer.style.display = "flex";
  }

  if (sound) {
    sound.currentTime = 0;
    sound.play().catch(() => {});
  }

  setTimeout(() => {
    if (layer) {
      layer.style.display = "none";
    }
  }, 4000);
}

